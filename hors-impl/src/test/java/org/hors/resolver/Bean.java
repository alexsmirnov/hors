package org.hors.resolver;

public class Bean {

	private String name;

	public Bean() {
	}

	public Bean(String name) {
		this.name = name;
	}
	
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
}
